package Livraria;

public class Livro {

	public String titulo;
	public String autor;
	public String genero; 
	public Double preco;
	
	public Livro(String titulo, String autor, String genero, Double preco) {
		super();
		this.titulo = titulo;
		this.autor = autor;
		this.genero = genero;
		this.preco = preco;
	}
	public Livro() {
		
	}
}